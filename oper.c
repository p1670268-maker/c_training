#include<stdio.h>
void main()
{
    int n1 = 90;
    int n2 = 12;
    int sum = n1 + n2;
    int sub = n1 - n2;
    int prod = n1 * n2;
    int quo = n1 / n2;
    int rem = n1 % n2;
    printf("addition: %d \n",sum);
    printf("subtraction: %d \n",sub);
    printf("multiplication: %d \n", prod);
    printf("division: %d \n", quo);
    printf("remainder: %d \n",rem);
}